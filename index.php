<script>
(function () {
  const tabs = document.querySelectorAll('[data-pms-tab]');
  const panels = document.querySelectorAll('[data-pms-panel]');

  tabs.forEach((tab) => {
    tab.addEventListener('click', () => {
      const target = tab.getAttribute('data-pms-tab');

      tabs.forEach((item) => {
        const isActive = item === tab;
        item.classList.toggle('active', isActive);
        item.setAttribute('aria-selected', isActive ? 'true' : 'false');
      });

      panels.forEach((panel) => {
        const isActive = panel.getAttribute('data-pms-panel') === target;
        panel.classList.toggle('active', isActive);
        panel.hidden = !isActive;
      });

      const link = document.querySelector('[data-pms-link]');
      if (link) {
        const label = target === 'catalyst' ? 'Catalyst' : 'Fusion';
        link.href = target + '.php';
        link.textContent = 'Know more about ' + label;
      }
    });
  });
})();
</script>

